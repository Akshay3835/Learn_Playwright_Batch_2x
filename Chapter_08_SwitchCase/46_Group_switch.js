let browser = "Edge";

switch ( browser ) {
    case "Chrome":
    case "Edge":
    case "Brave":
    case "Opera":
        console.log( "Chrome Project" );
        break;
    case "FireFox":
        console.log( "Mozilla Project" );
        break;

    case " Safari":
        console.log( "Apple Browser" );
        break;
    default:
        console.log( "Unknown browser - go to manual entry" )

}

