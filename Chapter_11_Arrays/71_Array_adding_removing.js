let arr = [1, 3, 5];
console.log( arr );

// Add to END
arr.push( 6 );
console.log( arr );

// Remove from END
arr.pop();
console.log( arr );


arr.push( 2, 4 );
console.log( arr );

// Add to Beggining
arr.unshift( 2 );
console.log( arr );

// remove from Beggining
arr.shift();
console.log( arr );

